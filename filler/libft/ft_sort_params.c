#include "libft.h"

void	ft_sort_params(char **str)
{
	char **p;
	char **c;

	p = str;
	c = str;
	while (*(p + 1) != '\0')
	{
		if (ft_strcmp(*p, *(p + 1)) > 0)
		{
			*c = *p;
			*p = *(p + 1);
			*(p + 1) = *c;
			p = str;
		}
		p++;
	}
}
